# arithmetic operator
x=7
y=3
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)

# comparison operator
a=12
b=34

print(f"variable {a} is equal to {b}{a==b}")
print(f"variable {a} is equal to {b}{a!=b}")
print(f"variable {a} is equal to {b}{a>b}")
print(f"variable {a} is equal to {b}{a>=b}")
print(f"variable {a} is equal to {b}{a<b}")
print(f"variable {a} is equal to {b}{a<=b}")

# logical operators
c=5
print(f"is this statement true? {c>2 and c<5}")
print(f"One of the condition is true? {c>2 or c<=5 }")

